    public void fillToTable() {
        DefaultTableModel model = (DefaultTableModel) tblChiTiet.getModel();
        model.setRowCount(0);
        try {
            list = dao.selectAll(txtMaHDN.getText());
            int i = 1;
            for (ChiTietHDN ch : list) {
                Object[] row = {
                    i,
                    ch.getMaXe(),
                    ch.getTenXe(),
                    ch.getGiaTienNhap(),
                    ch.getSoLuong(),
                    ch.getGiaTienNhap()*ch.getSoLuong()
                };
                tong=tong+ch.getGiaTienNhap()*ch.getSoLuong();
                model.addRow(row);
                i++;
            }
            NumberFormat formatter = new DecimalFormat("#,###,###");
            lblTongTien.setText(formatter.format(tong));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "lỗi truy vẫn dữ liệu");
        }
    }
    
    private boolean check(){
        boolean ok = true;
        StringBuilder bd = new StringBuilder();
        CheckLoi.checkNgayNhap(txtNgayNhap, bd);
        if (bd.length()>0) {
            JOptionPane.showMessageDialog(this,bd.toString(),"Thông báo",
                    JOptionPane.ERROR_MESSAGE);
            ok = false;
        }
        return ok;
    }

