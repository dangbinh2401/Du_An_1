    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int i = tblChiTiet.getSelectedRow();
        if (i != -1) {
            ThemChiTietHoaDonNhap cs = new ThemChiTietHoaDonNhap(null,true);
            cs.btnThem.setText("Chỉnh sửa");
            cs.txtMaHDN.setText(list.get(i).getMaHDN());
            cs.txtMaHDN.disable();
            cs.cboMaXe.setSelectedItem(list.get(i).getMaXe());
            cs.txtSoLuong.setText(String.valueOf(list.get(i).getSoLuong()));
            cs.txtGiaTien.setText(String.valueOf(list.get(i).getGiaTienNhap()));
            cs.SoLuongGoc=list.get(i).getSoLuong();
            cs.show();
            fillToTable();
        } else {
            JOptionPane.showMessageDialog(this, "vui lòng chọn sản phẩm cần sửa ");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void btnThemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnThemMouseClicked
        if (check()) {
            ThemChiTietHoaDonNhap them = new ThemChiTietHoaDonNhap(null,true);
            them.txtMaHDN.setText(txtMaHDN.getText());
            them.txtMaHDN.disable();
            them.show();
            fillToTable();    
        }
    }//GEN-LAST:event_btnThemMouseClicked

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int i = tblChiTiet.getSelectedRow();
        if (i!=-1){
            if (JOptionPane.showConfirmDialog(this, "bạn có chắc muốn xóa sản phẩm này?")==0){
                dao.delete(list.get(i).getMaHDN(), list.get(i).getMaXe());
            JOptionPane.showMessageDialog(this, "Xóa thành công!");
            fillToTable();
            }
        }else{
            JOptionPane.showMessageDialog(this, "Vui lòng chọn sản phẩm cần xóa!");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

