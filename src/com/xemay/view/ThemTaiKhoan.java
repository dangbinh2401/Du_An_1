    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (btnThem.getText().equals("Thêm")) {
            this.them();
        } else {
            this.chinhSua();
        }
    }//GEN-LAST:event_btnThemActionPerformed
    TaiKhoanDAO tk = new TaiKhoanDAO();

    private boolean check(){
        boolean ok = true;
        StringBuilder bd = new StringBuilder();
        CheckLoi.checkRong(txtMaTK, bd,"Mã chưa nhập\n");
        CheckLoi.checkRong(txtMatKhau, bd,"Mật khẩu chưa nhập\n");
        CheckLoi.checkRong(txtVaiTro, bd,"Vai trò chưa nhập\n");
        if (bd.length() > 0) {
            JOptionPane.showMessageDialog(this,bd.toString(),"Thông báo",
                    JOptionPane.ERROR_MESSAGE);
            ok = false;
        }
        return ok;
    }
    
    void them() {
        if (check()) {
            try {
            tk.insert(model());
            JOptionPane.showMessageDialog(this, "Thêm tài khoản thành công !");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,"Thêm tài khoản thất bại !","Thông báo",
                    JOptionPane.ERROR_MESSAGE);
            }
        }
    }

    void chinhSua() {
        if (check()) {
            try {
            tk.update(model());
            JOptionPane.showMessageDialog(this, "Cập nhật tài khoản thành công !");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,"Cập nhật tài khoản thất bại !","Thông báo",
                    JOptionPane.ERROR_MESSAGE);
            }
        }
    }

    TaiKhoan model() {
        TaiKhoan model = new TaiKhoan();
        model.setMaTk(txtMaTK.getText());
        model.setMatKhau(txtMatKhau.getText());
        model.setVaiTro(txtVaiTro.getText());
        return model;
    }

